-- C1 (drop TelegramID from S3 avatar key path) закрыл утечку только для
-- НОВЫХ аплоадов. Существующие avatar_url продолжают вести на ключи вида
-- avatars/<telegram_id>/<...> — presigned URL у клиента всё ещё палит ID.
--
-- Сбрасываем avatar_url у всех затронутых записей. На следующем OAuth-логине
-- бот зальёт фотку под новый uuid-ключ, а handler.HandleBotMessage запишет
-- её в БД (см. C2: при пустом avatar_url повторная синхронизация работает).
--
-- Кастомные аватарки, загруженные через POST /members/me/avatar, тоже
-- сидели на пути avatars/<telegram_id>/<uuid>.<ext>, поэтому слетят
-- вместе со всеми. Без бэкенд-стороннего S3-копирования сохранить их
-- иначе нельзя; пользователю придётся перезагрузить.
--
-- Регулярка покрывает оба варианта: и сырой ключ (avatars/123/...), и
-- вариант, где в БД оказался полный presigned URL (.../avatars/123/...?...).
UPDATE members
SET avatar_url = ''
WHERE avatar_url ~ '(^|/)avatars/[0-9]+/';
